    }
}

fun normalizedApiUrl(): String {
    val configured = localProperties.getProperty("SCHOOLFLOW_API_URL")
        ?: "http://10.0.2.2:8000/api/"
    return if (configured.endsWith("/")) configured else "$configured/"
}

